// Precise regexes (RTL question-mark artifact + placeholder review images).
const REGEXES = [
  [/\?מה משפיע על מחיר שכפול מפתח לרכב/g, 'מה משפיע על מחיר שכפול מפתח לרכב?'],
  [/<img[^>]*src="https:\/\/via\.placeholder\.com\/[^"]*"[^>]*>/gi, ''],
];

let totalFiles = 0, totalReps = 0;
for (const name of fs.readdirSync(DIR)) {
  if (!name.endsWith('.ts') || name === '_type.ts') continue;
  const fp = path.join(DIR, name);
  let s = fs.readFileSync(fp, 'utf8');
  const before = s;
  let n = 0;
  for (const [f, r] of PAIRS) {
    if (s.includes(f)) { const c = s.split(f).length - 1; s = s.split(f).join(r); n += c; }
  }
  for (const [re, r] of REGEXES) {
    s = s.replace(re, () => { n++; return r; });
  }
  if (s !== before) { fs.writeFileSync(fp, s); totalFiles++; totalReps += n; }
}
console.log(`Spelling: ${totalReps} fixes across ${totalFiles} files.`);
